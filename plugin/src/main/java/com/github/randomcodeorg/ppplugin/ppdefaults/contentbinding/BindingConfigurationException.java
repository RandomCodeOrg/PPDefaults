package com.github.randomcodeorg.ppplugin.ppdefaults.contentbinding;

public class BindingConfigurationException extends RuntimeException {

	private static final long serialVersionUID = 2885805733109280979L;

	public BindingConfigurationException() {
		super();
		
	}

	public BindingConfigurationException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

	public BindingConfigurationException(String message, Throwable cause) {
		super(message, cause);
		
	}

	public BindingConfigurationException(String message) {
		super(message);
		
	}

	public BindingConfigurationException(Throwable cause) {
		super(cause);
		
	}

	
	
}
